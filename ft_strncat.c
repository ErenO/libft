#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*p1;
	char	*p2;

	p1 = s1;
	p2 = (char *)s2;
	while (*p1)
		p1++;
	while (*p2 && n--)
	{
		*p1 = *p2;
		p1++;
		p2++;
	}
	*p1 = '\0';
	return (s1);
}
